/** Barcha tarjima kalitlari uchun tip (uz — manba shakl). */
export type Dictionary = typeof uz;

/** Telefon holati kodini (NEW/USED/REFURBISHED) joriy tilга o'giradi. */
export function conditionLabel(condition: string, t: Dictionary): string {
  switch (condition) {
    case "NEW":
      return t.phoneForm.conditionNew;
    case "USED":
      return t.phoneForm.conditionUsed;
    case "REFURBISHED":
      return t.phoneForm.conditionRefurb;
    default:
      return condition;
  }
}

/** Kafolat muddatini (oy) joriy tilда yozadi: 0→Kafolatsiz, 12→1 yil. */
export function warrantyLabel(months: number, t: Dictionary): string {
  if (!months) return t.phoneForm.noWarranty;
  if (months % 12 === 0) return `${months / 12} ${t.inventory.year}`;
  return `${months} ${t.inventory.months}`;
}

/** To'lov turi kodini (CASH/CARD/CREDIT) joriy tilга o'giradi. */
export function paymentTypeLabel(type: string, t: Dictionary): string {
  switch (type) {
    case "CASH":
      return t.sellModal.cash;
    case "CARD":
      return t.sellModal.card;
    case "CREDIT":
      return t.sellModal.credit;
    default:
      return type;
  }
}

/** Mijoz qarzi holati kodini joriy tilга o'giradi. */
export function customerStatusLabel(status: string, t: Dictionary): string {
  switch (status) {
    case "ACTIVE":
      return t.customers.statusActive;
    case "PAID":
      return t.customers.statusPaid;
    case "OVERDUE":
      return t.customers.statusOverdue;
    case "CANCELLED":
      return t.customers.statusCancelled;
    default:
      return status;
  }
}
